package com.wegooooo.ndk.mnn;

public class LaneInfo {

    public float x0, y0, x1, y1, lens, conf;


    public LaneInfo(float x0, float y0, float x1, float y1, float lens, float conf) {
        this.x0 = x0;
        this.y0 = y0;
        this.x1 = x1;
        this.y1 = y1;
        this.lens = lens;
        this.conf = conf;
    }
}
